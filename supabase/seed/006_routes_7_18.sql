ОПИСАНИЕ МАРШРУТА:

R0-R1. По снежному склону (лавиноопасно!). Летом по осыпи и мокрым «бараньим лбам» с косыми полками, маршрут удобнее начинать значительно правее «зимней линии», по косым полкам обхода мокрые «бараньи лбы» к «скальным воротам», (II кат.сл., 200 м, 30°).

R1-R2. По снежному склону (летом по «бараньим лбам») через «скальные ворота» под начало гребня. Первый «жандарм» с контрольным туром (банка на крюке), (170 м, до 45°, II кат.сл.), лавиноопасно. Подъём на «жандарм» справа по разрушенным скалам под стенки следующего «жандарма» (20 м, до 45°, II+ кат.сл.).

R2-R3. По небольшим стенкам с крутыми гребневыми вершинами подход под следующий «жандарм» (имеет характерную клыкообразную форму), (60 м, 60°, III+ кат.сл.).

R3-R4. Ключевой участок маршрута; по стенке, подъём на «жандарм» с правой стороны, выход на узкую перемычку с карнизами перед следующей стенкой (40 м, 75°, IV кат.сл.).

R4-R5. По узкой перемычке и небольшой стенке со вторым контрольным туром (банка на крюке), (5 м, 90°, IV кат.сл.), далее по узкому крутому участку гребня с карнизами (25 м, 45°, III кат.сл.), выход на выполаживание, станция на ледорубах.

R5-R6. По гребню выход на узкую полку под предвершинным взлётом, над полкой большие снежные карнизы (100 м, 10°, II кат.сл.).

R6-R7. С горизонтальной полки по крутой наклонной полке влево под карниз, (30 м, 60°, III кат.сл.).

R7-R8. Из-под карниза влево и вверх по наклонным полкам (много тонкого натечного льда.) выход на плато (30 м, 60°, III+ кат.сл.). Контрольный тур выложен камнями на плато. Зимой он недоступен.

