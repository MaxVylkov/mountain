R14-R15. Вверх по расщелине (40 м, 70°, III+ кат.сл.). Место страховки можно оборудовать петлей за выступ на полке.

R15-R16. По стенке (4 м, 80°, IV кат.сл.) через полку (5 м) и камин, уход влево вверх по разрушенным скалам (30 м, 40°, II кат.сл.), выход на вершину (плато). Контрольный тур выложен камнями на плато.

