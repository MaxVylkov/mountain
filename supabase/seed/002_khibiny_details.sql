R1-R2. Двигаться вправо по ребру, вверх до основания внутреннего угла (летом двигаться вверх под стену, траверс вправо). Редкая страховка и сбросы справа. Удобно расположить станцию по внутренним углом (60 м, 25°, I-II кат.сл.). Площадка под станцию небольшая. Контрольный тур №2 (банка на крюке).

