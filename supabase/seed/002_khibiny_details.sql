ПОДХОД ПОД МАРШРУТ: опорной точкой (базовым лагерем) можно взять г. Кировск. От города двигаться на машине по асфальтированной дороге в сторону Апатитов примерно 5 км, далее, напротив старого аэродрома, свернуть направо по ходу движения и двигаться по грунтовой дороге до ж/д переезда, после переезда свернуть направо. После поворота двигаться ещё 2 км, свернуть на характерную лесную дорогу и придерживаться северного направления. Началом подхода является характерная площадка и установленная опора ЛЭП. Ориентир — массив «Расчёска», просматривающийся практически со всех участков пути.

ОПИСАНИЕ МАРШРУТА:
R0-R1. Начало движения по некрутому лугу, переходящему в каменную осыпь. После дождя может быть скользко.

R1-R2. Далее, обходя левее крутую каменную стенку, по замшелым полкам подняться вверх до удобной полки. Тут можно организовать удобную станцию.

R2-R3. Налево, вверх по замшелой полке до характерного внутреннего угла, сложенного из крупных плит и камней. Рекомендуем применение средних камалотов. Далее вверх, пройти наклонную замшелую полку и выйти через небольшой уступ на вторую полку до скальной стенки.

R3-R4. Преодолеть несложную стенку 5 м, и по наклонной полке выйти под скальную стенку, держась левее.

R4-R5. От станции взять левее и по развальной (вначале) стенке подняться вверх на выполаживание.

Рекомендации по маршруту: маршрут лавиноопасен в зимнее время, начиная со 2 степени лавинной опасности. Рекомендуется прохождение летом.
Протяжённость участков: I кат.сл. — 200 м, II кат.сл. — 50 м, III кат.сл. скалы — 15 м.
Рекомендуемое ходовое время: 3,5 часа.
Спуск с вершины: проходит в южной части горы, через лесистую местность и выводит к лесной дороге. Ориентирами могут служить г. Кировск и линия канатно-кресельного подъёмника на гору Айкуайвенчорр.'
