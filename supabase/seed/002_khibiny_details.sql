Вершина: Айкуайвенчорр, Восточная.

ПОДХОД ПОД МАРШРУТ: необходимо пройти пешком 40-50 мин. в цирк ВМФ до начала маршрута. Подход осуществляется по старой грунтовой дороге от ж/д путей. Двигаться в цирк, в любом удобном месте перейти реку вброд, далее в сторону северной стены г. Айкуайвенчорр.

ОПИСАНИЕ МАРШРУТА:
R0-R1. По каменной перемычке перейти налево. Подняться вверх на большую полку. Хорошее место для сбора группы.

R1-R2. Двигаться по полкам широкого контрфорса, придерживаясь правой стороны. У характерного заметного места (с отколовшимся куском скалы) повернуть налево и продолжить движение вверх.

R2-R3. Придерживаясь правее внутреннего угла, выйти на полку.

R3-R4. Несложное одновременное передвижение по наклонным плитам и полкам. Избегать сильных траверсов в стороны.

R4-R5. По правой широкой стенке камина двигаться вверх, в средней части есть узкая полка и рельеф для установки точек страховки. Далее следует выходить левее на широкую наклонную полку.

R5-R6. Слева от полки находится характерная трещина, удобная для страховки. Как ориентир направления лазания, участок слева, по наклонной полке, не находится — сложный выход к перемычке лазанием.

R6-R7. Ключевой участок. Придерживаться в лазании правее трещины по стене. Есть много доступных точек для страховки и рельефа для лазания. Слева и справа присутствуют кулуары. Кулуары опасны весной камнями, зимой лавинами, летом там сложно организовать надёжную страховку. После выхода на полку есть много мест для организации станции. Выход на плато и путь на вершину.

Протяжённость участков: I кат.сл. — 10 м, II кат.сл. — 200 м, III кат.сл. — 35 м.
Крутизна основной части маршрута: 55,4°.
Ориентировочное ходовое время: 4 часа.
Спуск с маршрута: в летнее время через кулуар за маршрутом, либо через вершину г. Айкуайвенчорр двигаться к горнолыжному комплексу «Большой Вудъявр».

Дополнительная информация: на отроге горы есть «домик фрирайдера». Первоначально в нём работали радисты ВМФ. В домике есть печь, дров и воды нет. Здесь можно переждать непогоду, но домик в плохом состоянии.'
