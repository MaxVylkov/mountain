ПОДХОД ПОД МАРШРУТ: пройти до железнодорожного моста над дорогой, далее по путям до 165 столба, после через лес по тропе до пересечения со старым зимником (в цирк ВМФ), далее двигаться к подножию отрога (1-1,5 часа).

ОПИСАНИЕ МАРШРУТА:
Начало маршрута от крупного, одиноко стоящего камня. От него двигаться вверх по снегу, летом по каменной осыпи 150 м, 25°.

