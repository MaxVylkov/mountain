R3-R4. Пересечение поля к началу скального бастиона. Зимой лавиноопасно, нужна страховка (60 м, 25°, I кат.сл.). Контрольный тур №3 (банка на крюке).

R4-R5. Траверс вправо под стеной бастиона (50 м, 25°, II кат.сл.), место под станцию, не доходя до выраженного внутреннего угла с гротом.

R5-R6. Движение до грота (10 м, 30°, II кат.сл.). Далее во внутренний угол по серии полок (20-25 м, III-IV кат.сл., 40-55°), выход к выполаживанию до скальных блоков (20 м, 15-20°, I кат.сл.). Летом движение до грота 10 м, траверс вправо 25 м, подъём по разрушенным скалам вверх, обходя каменный палец 30 м. Слева проход в расщелину, камнеопасно (5 м, 20°), дальше — серия полок и небольших стенок, лазание I-II кат.сл., 20 м.

Выход на выполаживание. Далее выход на плато и одновременное движение до вершины. На маршруте имеется 3 тура.

