UPDATE routes SET description = 'Категория: 4А. Первопроход: Шпилевой Д.А., март 2018.
Вершина: Айкуайвенчорр, Центральная.

ОПИСАНИЕ МАРШРУТА:
R0-R1. По крутому снежно-фирновому склону подойти под начало маршрута: ярко выраженный внутренний угол. Здесь первая станция.

R1-R2. Вверх по внутреннему углу, затем через заглаженную плиту с трещинами. Далее ещё один внутренний угол, на выходе — станция.

R2-R3. От станции немного вправо и влево вверх в маленький внутренний угол. Затем сложная заглаженная стенка. Сначала вверх, затем траверсом вверх и влево в направлении очередного внутреннего угла. Лазание сложное, рельеф бедный. Здесь станция.

R3-R4. Вверх вправо по «бараньим лбам» до вертикальной стенки.

R4-R5. Далее вверх вправо по крутому снежному склону («бараньи лбы»), подходим к вертикальной стенке и двигаемся вдоль неё траверсом в направлении маленького внутреннего угла (камин). Проходим сложный вертикальный внутренний угол и по наклонной полке к вертикальной стенке. Здесь станция.

R5-R6. От станции вправо во внутренний угол. Лазание сложное. Далее прямо вверх в основание огромного внутреннего угла. Здесь станция.

R6-R7. От станции по большому внутреннему углу, затем вправо в направлении камина. Проходим камин — лазание сложное. После вверх к стенке, здесь станция.

R7-R8. Ключевая верёвка. Влево в большой внутренний угол, прямо вверх, затем траверсом вправо. Лазание сложное, рельеф бедный. Затем выходим на наклонную полочку. Слева виднеется сложный слегка нависающий внутренний угол — туда не надо. Обходим его справа вверх по камину, попадаем в заглаженный внутренний угол, оттуда через левое ребро угла перелезаем влево вверх в соседний внутренний угол. Здесь также сложное лазание с бедным рельефом. Далее вверх по серии внутренних углов под вертикальную слегка нависающую стенку, станция.
Участок R7-R8, где слегка нависающий внутренний угол, обходим справа, возможно стоит лезть «в лоб». Лазание сложнее, но линия немного короче. Вариант для сильных спортивных групп.

R8-R9. Влево во внутренний угол, после слево вверх через левое ребро угла, перебираемся на наклонную полочку и через вертикальную стенку выходим на выполаживающийся рельеф. Движемся в направлении скал «Паруса». Здесь станция.

R9-R10. Далее прямо вверх, через маленькую стенку подходим к большой вертикальной стене. Здесь нужно спуститься влево вниз на наклонную полочку. По полочке поднимаемся вверх на выраженный гребень, станция.

R10-R11. От станции по гребню до большого «жандарма», за ним откол, на нём станция.

R11-R12. Вверх к вертикальной стенке, которая проходится влево вверх по внутреннему углу. После по гребню на «крышу». Станция на ледорубах.

R12-R13. Влево по гребню на вершину. Осторожно! Слева надувает мощные карнизы.

Протяжённость участков: I кат.сл. — 710 м, II кат.сл. — 30 м, III кат.сл. — 298 м, IV кат.сл. — 75 м, V кат.сл. — 50 м.
Средняя крутизна стенной части: 68°.
Ориентировочное время прохождения маршрута: 10 часов.
Используемое снаряжение: якорей — 24, френдов — 25, стопперов — 14, рекомендуется большое количество якорных крючьев, френдов и наличие комплекта ИТО.
Спуск с вершины: вначале по гребню по пути подъёма, затем далее вниз по гребню вдоль стальных столбов до перевала ВМФ. С перевала вправо спуск по линии падения воды (возможно, лавиноопасно!) и выход на подъёмные следы.'
