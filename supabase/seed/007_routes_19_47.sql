ПОДХОД ПОД МАРШРУТ: через перевал Рамзая и Западный Петрелиус.

ОПИСАНИЕ МАРШРУТА:
Приведённая схема УИАА взята из описания Андреева А.Б. 2003 г. и достаточно точно описывает линию маршрута. Длины участков соответствуют описанию за исключением участка R9-R10. Для удобства прохождения и организации страховки участок целесообразно разбить на 2, как указано на схеме.

Начало маршрута лежит по снежнику (летом снег жёсткий, слежавшийся, местами наледь).

R1-R2. Подъём по крутому снежнику под скалы (200 м, 30°, II кат.сл.).

R2-R3. Со снега переход на плиту (5 м, 60°, III кат.сл.) до наклонной полки. По полке траверс влево (20 м, 30°, II кат.сл.). Стенка (5 м, 60°, IV кат.сл.), траверс направо и выход на террасу (20 м, 30°, II кат.сл.).

R3-R4. По разрушенному внутреннему углу (35 м, 70°, IV+ кат.сл.) до вертикальной стенки (правая образующая внутреннего угла). По стенке (15 м, 90°, V+ кат.сл.), подъём на неудобной полки. Контрольный тур № 1. Лазание 6a. На участке хорошо иметь френд № 4 и 5.

R4-R5. Обход карниза справа (5 м, 95°, V+ кат.сл.), выход во внутренний угол, по углу и его правой стене (40 м, 80°, V кат.сл.). Карниз обходится справа с выходом на большую полку. Опасно при камнепадах! Местами проблематично организовать надёжную страховку. На полке возможна организация ночёвки. Лазание 6b.

R5-R6. По стене вверх влево (30 м, 70°, IV кат.сл.), выход на наклонную полку.

R6-R7. По характерному вертикальному внутреннему углу (40 м, 90°, V+ кат.сл.), до полки.

R7-R8. По стене вверх (30 м, 80°, V кат.сл.), выход на удобную полку в основании внутреннего угла.

R8-R9. По внутреннему углу вправо вверх (45 м, 65°, IV+ кат.сл.) до вертикального внутреннего угла.

R9-R10. От внутреннего угла траверс вертикальной стенки вправо (10 м, 90°, VI кат.сл.). По вертикальной стенке вверх (15 м, 90°, V+ кат.сл.). Полка. Лазание 6a.

R10-R11. По вертикальной стенке вверх под карниз (15 м, 90°, VI кат.сл.). Под карнизом шлямбур (съёмный «спит» без проушины). Через карниз по глухой щели (20 м, 90°, A3, VI кат.сл.), выход на небольшую полку под стенку. Траверс вправо (7 м, 90°, V кат.сл.), по стенке вверх (110 м, 85°, V+ кат.сл.). Узкая полка, шлямбур (съёмный). Контрольный тур № 2.

Для ИТО рекомендуем использовать якорные крючья, фифи, средние френды. Лазание 6b. В начале участка проблематично организовать страховку.

R11-R12. С полки траверс влево (5 м, 85°, V кат.сл.) под начало внутреннего угла. По внутреннему углу, образованному серией отколов (10 м, 85°, V+ кат.сл.). Далее по стенке вверх до полки (15 м, 80°, V кат.сл.).

R12-R13. По полке траверс вправо вверх (15 м, 80°, IV кат.сл.) под нависающий внутренний угол. По углу (5 м, 95°, A1, VI кат.сл.), выйти на полку. С полки вверх по стене (15 м, 70°, V+ кат.сл.) войти в основание разрушенного камина. По камину (15 м, 60°, III кат.сл.). Станция в камине, место неудобное. Съёмный шлямбур.

Участок ИТО проходится лазанием 6b.

R13-R14. По камину (10 м, 75°, V кат.сл.) до полки, затем влево по внутреннему углу (30 м, 70°, IV кат.сл.). Выход на вершину.

Рекомендации по маршруту: Маршрут представляет собой логичную, хорошо читаемую линию, которая привязана к внутренним углам, расположенным на стене. Линия полностью проходится свободным лазанием (уровень 6b-6b+ по французской классификации), за исключением участка R10-R11, который представляет собой глухую сырую щель протяжённостью около 20 м уровня сложности лазания ИТО А2+-А3.

Ориентирование на маршруте затруднено в районе R6-R7, так как после прохождения участка выход будет находиться напротив огромного внутреннего угла, заканчивающегося карнизами, что является ложным путём. Линия маршрута проходит левее угла, по менее выраженному, но вертикальному внутреннему углу. Дальнейшее ориентирование логичное и полностью соответствует описанию маршрута.

Организация страховки на маршруте местами представляет сложности, так как рельеф является монолитной скалой. Все страховочные станции маршрута находятся в безопасных местах, большинство на полках.

Решением команды участок R9-R10 в целях исключения дополнительных перегибов верёвки разделён на два. Встречающиеся съёмные шлямбуры («спиты») первопроходцев без проушин, местами с ржавчиной, проушины до конца не накручиваются.

Рекомендации по маршруту: для прохождения маршрута в зимнее время рекомендуется пара инструментов, щётка для снега, кошки всем участникам. Готовность лидера к работе на «бедном» рельефе.

В условиях полярного дня (лето) возможно прохождение маршрута в двойке в альпийском стиле. Воды на маршруте нет.

Протяжённость участков: V кат.сл. — 250 м, VI кат.сл. — 50 м.
Средняя крутизна: средняя крутизна основной части маршрута 67°, средняя крутизна основной части 79°.
Ориентировочное время прохождения маршрута: 22 часа.

Спуск с вершины: по восточному отрогу г. Петрелиуса. Применения альпинистского снаряжения на спуске не требуется.
Аварийный выход с маршрута: по пути подъёма.'
