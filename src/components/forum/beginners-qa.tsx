export function BeginnersQA() {
  const [open, setOpen] = useState<string | null>(null)

  return (
    <div className="space-y-8">
      <div>
        <p className="text-sm text-mountain-muted leading-relaxed">
          Собрали самые частые вопросы новичков с развёрнутыми ответами. Если не нашёл свой вопрос — задай его в разделе «Обучение».
        </p>
      </div>

      {SECTIONS.map(section => (
        <section key={section.title}>
          <h2 className="text-xs font-semibold uppercase tracking-[0.14em] text-mountain-muted mb-3">
            {section.title}
          </h2>
          <div className="space-y-1">
            {section.items.map(item => {
              const id = `${section.title}:${item.q}`
              const isOpen = open === id
              return (
                <div key={item.q} className="border border-mountain-border rounded-lg overflow-hidden">
                  <button
                    type="button"
                    aria-expanded={isOpen}
                    onClick={() => setOpen(isOpen ? null : id)}
                    className="w-full flex items-start justify-between gap-3 px-4 py-3 text-left hover:bg-mountain-border/30 transition-colors"
                  >
                    <span className="text-sm font-medium text-mountain-text">{item.q}</span>
                    <ChevronRight
                      size={15}
                      className={`shrink-0 mt-0.5 text-mountain-muted transition-transform duration-200 ${isOpen ? 'rotate-90' : ''}`}
                    />
                  </button>
                  {isOpen && (
                    <div className="px-4 pb-4 pt-1 border-t border-mountain-border bg-mountain-bg/40">
                      <p className="text-sm text-mountain-muted leading-relaxed">{item.a}</p>
                    </div>
                  )}
                </div>
              )
            })}
          </div>
        </section>
      ))}
    </div>
  )
}
